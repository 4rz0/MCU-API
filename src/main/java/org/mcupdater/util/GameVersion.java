package org.mcupdater.util;

public class GameVersion {
	private int id;
	private int gameVersionTypeID;
	private String name;
	private String slug;

	public GameVersion() {

	}

	public int getId() {
		return id;
	}

	public int getGameVersionTypeID() {
		return gameVersionTypeID;
	}

	public String getName() {
		return name;
	}

	public String getSlug() {
		return slug;
	}
}
